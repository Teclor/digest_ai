class PromptTemplateFactory:
    """
    Фабрика промптов: возвращает шаблон промпта в зависимости от типа текста.
    """

    @staticmethod
    def get_prompt(template_type: str) -> str:
        templates = {
            "news": "Создай краткий дайджест текста, сохранив ключевые смыслы и структуру. Исключи второстепенные детали, повторы и служебные формулировки. Передавай прямую речь в косвенной форме, используя сжатые логически связанные предложения. Фокусируйся на фактах, действиях, решениях и их последствиях.\n\n{text}",
            "chat": "Создай краткий пересказ рабочего чата, выделяя ключевые рабочие моменты: задачи, решения, обновления, назначения. Структурируй информацию в логические блоки по темам. Исключи приветствия, эмодзи, флуд, рутинные согласования и повторы. Переводи прямую речь в косвенную, фокусируясь на действиях, сроках и ответственных. Сохраняй точность смыслов без искажений.\n\n{text}"
        }

        default_template = "Перескажи следующий текст кратко:\n\n{text}"

        return templates.get(template_type, default_template)